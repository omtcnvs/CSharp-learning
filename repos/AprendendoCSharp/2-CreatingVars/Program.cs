using System;
using System.Runtime.CompilerServices;
using System.Runtime.ConstrainedExecution;

class Program
{
    static void Main(string[] args)
    {

        Console.WriteLine("Project 2 - Creating Vars");

        int idade;

        idade = 27;

        Console.WriteLine(idade);

        idade = 27 - 5;

        Console.WriteLine(idade);

        idade = 5 * 2 - 6;

        Console.WriteLine(idade);

        idade = (5 - 2) * 3;

        Console.WriteLine(idade);

        Console.WriteLine("Type enter to close application ...");
        Console.ReadLine();

    }
}
